# programa que solicita dois números ao usuário e imprime o resultado da soma dos números informados

# solicita um número e já realiza a conversão para inteiro
numero01 = int(input("Informe um número: "))
# solicita outro número e também efetiva a conversão para inteiro
numero02 = int(input("Informe outro número: "))
# realiza a soma dos números informados
soma = numero01 + numero02
# apresenta o resultado da soma
print("A soma dos números informados é ", soma)